using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Net;
using System.Collections.Specialized;

namespace Kasim.Framework.Common
{
    static public class WebClientHttp
    {
        static public string Post(string url, NameValueCollection postVars)
        {
            WebClient webClient = new WebClient();
            try
            {
                byte[] byRemoteInfo = webClient.UploadValues(url, "POST", postVars);
                string strTokenInfo = Encoding.UTF8.GetString(byRemoteInfo);
                return strTokenInfo;
            }
            catch (Exception ex)
            {
                FlashLogger.Error(ex.Message);
                return ex.Message;
            }
        }
    }
}
